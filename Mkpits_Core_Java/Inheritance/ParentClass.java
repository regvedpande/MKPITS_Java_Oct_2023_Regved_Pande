package com.mkpits.inheritance;

public class ParentClass {

	String firstname;
	String lastname;
	String email;

	public void getFirstName(String firstname) {

		this.firstname = firstname; // both these values are getting assigned to so that we can access these values
									// from anywhere
	}

	public void getLastName(String lastname) {

		this.lastname = lastname; // both these values are getting assigned to so that we can access these values
									// from anywhere

	}

	public void getEmail(String email) {

		this.email = email; // both these values are getting assigned to so that we can access these values
							// from anywhere

	}

}