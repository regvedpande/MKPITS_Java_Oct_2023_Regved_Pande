import java.util.ArrayList;

public class PlanetName {
    public static void main(String[] args) {
        // Creating an ArrayList of planet names
        ArrayList<String> planets = new ArrayList<>();

        // Adding planet names to the ArrayList
        planets.add("Mercury");
        planets.add("Venus");
        planets.add("Earth");
        planets.add("Mars");
        planets.add("Jupiter");
        planets.add("Saturn");
        planets.add("Uranus");
        planets.add("Neptune");

        // Printing the ArrayList of planet names
        System.out.println("Planet Names: " + planets);
    }
}
